    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
         String isbn = isbnText.getText();
         String title = titleText.getText();
         String id = idText.getText();
         String name = nameText.getText();
         String issue = ((JTextField)issueDate.getDateEditor().getUiComponent()).getText();  
         String due = ((JTextField)dueDate.getDateEditor().getUiComponent()).getText();
         
        if(isbn.equals("")|title.equals("")|id.equals("")|name.equals("")|issue.equals("")|due.equals("")){
            JOptionPane.showMessageDialog(null, "All Fields are Mandatory");
        }else{
            try {
                java.sql.Connection con = DbConnection.getCon();
                String query = "INSERT INTO `issue`(`isbn`, `title`, `id`, `name`, `issue_date`, `due_date`) VALUES (?, ?, ?, ?, ?, ?)";
                PreparedStatement pst = con.prepareStatement(query);
                pst.setString(1, isbn);
                pst.setString(2, title);
                pst.setString(3, id);
                pst.setString(4, name);
                pst.setString(5, issue);
                pst.setString(6, due);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Record Successfully");

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
            }
    }//GEN-LAST:event_addActionPerformed

